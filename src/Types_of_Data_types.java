public class Types_of_Data_types {
    //primitive and refrence
//    primitive contains values and refrence contains the address of the values

    public static void main(String[] args){
//        primitive
        int a = 8;
        System.out.println(a);
        double b =  6.465;
        System.out.println(b);
        String ab = "this is a string";
        System.out.println(ab);
        char thi = '@';
        System.out.println(thi);
        boolean fine = true;
        System.out.println(fine);

//        refrence
        String thit = "this is a string buddy";
        System.out.println(thit);
        System.out.println("this is a string called: " + thit + " and this is created by me");
    }
}
