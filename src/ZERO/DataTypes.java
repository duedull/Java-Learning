package ZERO;

public class DataTypes {

    //variables in java are of 2 types:

    //primitive and reference

    //primitive types

    //int, char, float, double, byte, boolean,short, long

    byte e = 127; // 8-bit signed integer ranging from -128 to 127.
    short g = 77; // 16-bit signed integer ranging from -32,768 to 32,767.
    int a = 1; //32-bit signed integer ranging from -2,147,483,648 to 2,147,483,647.
    long f = 333333; //64-bit signed integer ranging from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.
    float c = 11.3f; //32-bit floating-point number.
    double d = 5.756; //64-bit floating-point number.
    char b = '@'; //char: 16-bit Unicode character.

    //reference data types
    //String, Array, Interface, Class
    String h = "this is a string";
    int[] i = {1,2,3,4,5,6};
    //interface = collection of abstract methods
    //class = blueprint for creating objects
}