package DataStructures;
import java.util.HashSet;

//in hash set every element is unique
public class Hashset {
    public static void main(String[] args) {
        HashSet<String> Hs1 = new HashSet<>();
        Hs1.add("abhay");
        Hs1.add("singh");
        Hs1.add("is");
        Hs1.add("great");
        Hs1.add("buddy");

        System.out.println(Hs1);
    }
}
