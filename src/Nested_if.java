public class Nested_if {
    public static void main(String[] args) {
        int a = 7;

        if (a>=6){
            System.out.println("greater than 6");
        } else if (a == 6){
            System.out.println("equals to 6");
        }
        else {
            System.out.println("smaller than 6");
        }
    }
}
